function generateColorArray() {
  const shades = [500, 600, 700, 800, 900, 300, 400]
  return shades.flatMap((shade) =>
    TW_COLOR_NAME_CYCLE.map((color) => [`bg-${color}-${shade}`, `text-${color}-${shade > 400 ? 50 : 900}`])
  )
}

